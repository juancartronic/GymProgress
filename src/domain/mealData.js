export const adaptMealWeekByCalories = (week, calories) => {
  const tier = calories < 1900 ? "low" : calories > 2600 ? "high" : "mid";
  const adjustments = {
    low: {
      label: "Ajuste kcal: porciones moderadas y verduras extra",
      extras: [
        "porcion moderada de cereal",
        "verdura extra para saciedad",
        "snack ligero si hay hambre",
      ],
    },
    mid: {
      label: "Ajuste kcal: porciones estandar de mantenimiento",
      extras: [
        "porcion estandar",
        "fruta de postre opcional",
        "colacion simple si entrenas tarde",
      ],
    },
    high: {
      label: "Ajuste kcal: anade snack pre/post entreno",
      extras: [
        "anade snack pre entreno",
        "suma racion extra de carbohidrato",
        "incluye snack post entreno",
      ],
    },
  };
  const selected = adjustments[tier];
  return {
    ...week,
    calorieLabel: selected.label,
    days: week.days.map((day, index) => ({
      ...day,
      meals: `${day.meals} - ${selected.extras[index % selected.extras.length]}`,
    })),
  };
};
